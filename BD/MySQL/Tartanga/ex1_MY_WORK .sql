CREATE DATABASE IF NOT EXISTS MY_WORK;
USE  MY_WORK;

CREATE TABLE DEPARTMENT(
	D_No INTEGER NOT NULL PRIMARY KEY,
	D_Name VARCHAR (15),
	Town VARCHAR (15)
);

CREATE TABLE WORKER (
	Worker_No INTEGER NOT NULL PRIMARY KEY,
	Surname VARCHAR (10),
	Roles VARCHAR (10),
    Dir VARCHAR(10),
    Date_S Date,
    Salary INTEGER,
    Commission INTEGER,
    Dept_No INTEGER,
	FOREIGN KEY ( Dept_No) REFERENCES DEPARTMENT (D_No)
);

